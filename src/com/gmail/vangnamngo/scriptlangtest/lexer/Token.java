package com.gmail.vangnamngo.scriptlangtest.lexer;

public enum Token {
    // ========================================================================
    // Unary operators
    // ========================================================================

    NOT,
    ASSIGN,

    // ========================================================================
    // Binary operators
    // ========================================================================

    OPERATOR, // Includes: + - / * %
    SET_OPERATOR, // Includes: += -= /= *= %=

    // ========================================================================
    // Comparators
    // ========================================================================

    COMPARATOR, // Includes: == != < <= > >=

    AND,
    OR,

    // ========================================================================
    // Braces
    // ========================================================================

    L_PAREN,
    R_PAREN,
    L_BRACKET,
    R_BRACKET,

    BLOCK_START,
    BLOCK_END,

    // ========================================================================
    // Comments
    // ========================================================================

    SINGLE_COMMENT,

    START_FLEX_COMMENT,
    END_FLEX_COMMENT,

    // ========================================================================
    // Lang
    // ========================================================================

    HEADER_MARKER,

    KEYWORD,
    IDENTIFIER,

    STRING,
    BOOLEAN,
    NUMBER,

    DOT,
    COLON,
    COMMA,

    BACKSLASH
}
